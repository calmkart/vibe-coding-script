#!/bin/bash
set -euo pipefail

# ============================================================
#  Claude Code Auto-Approve Setup
#
#  Auto-approves Bash commands without manual confirmation.
#
#  Usage:
#    ./setup.sh install      # Install
#    ./setup.sh uninstall    # Remove
#    ./setup.sh status       # Show state
# ============================================================

CLAUDE_DIR="$HOME/.claude"
HOOKS_DIR="$CLAUDE_DIR/hooks"
SETTINGS_FILE="$CLAUDE_DIR/settings.json"
HOOK_SCRIPT="$HOOKS_DIR/auto-approve.sh"

info()  { printf '\033[1;34m[INFO]\033[0m  %s\n' "$1"; }
ok()    { printf '\033[1;32m[  OK]\033[0m  %s\n' "$1"; }
err()   { printf '\033[1;31m[ ERR]\033[0m  %s\n' "$1"; }

usage() {
    cat <<EOF
Claude Code Auto-Approve Setup

Usage:
  $(basename "$0") install      Install
  $(basename "$0") uninstall    Remove
  $(basename "$0") status       Show state
EOF
    exit 1
}

# ===========================================================
#  INSTALL
# ===========================================================
do_install() {
    info "Checking dependencies..."
    if ! command -v jq &>/dev/null; then
        err "jq not found — install with: brew install jq"
        exit 1
    fi
    ok "Dependencies OK"

    mkdir -p "$HOOKS_DIR"

    info "Creating auto-approve hook..."
    cat > "$HOOK_SCRIPT" << 'EOF'
#!/bin/bash
# Auto-approve all Bash commands (generated by auto-approve/setup.sh)
jq -n '{
  "hookSpecificOutput": {
    "hookEventName": "PreToolUse",
    "permissionDecision": "allow",
    "permissionDecisionReason": "Auto-approved by hook"
  }
}'
EOF
    chmod +x "$HOOK_SCRIPT"
    ok "Hook created: $HOOK_SCRIPT"

    info "Updating settings.json..."
    [ -f "$SETTINGS_FILE" ] || echo '{}' > "$SETTINGS_FILE"

    NEW_HOOK='{
      "matcher": "Bash",
      "hooks": [{"type": "command", "command": "~/.claude/hooks/auto-approve.sh"}]
    }'

    TEMP_FILE=$(mktemp)
    jq --argjson newHook "$NEW_HOOK" '
      .hooks //= {} |
      .hooks.PreToolUse //= [] |
      if (.hooks.PreToolUse | map(select(.matcher == "Bash" and .hooks[0].command == "~/.claude/hooks/auto-approve.sh")) | length) > 0
      then .
      else .hooks.PreToolUse += [$newHook]
      end
    ' "$SETTINGS_FILE" > "$TEMP_FILE"
    mv "$TEMP_FILE" "$SETTINGS_FILE"
    ok "Settings updated"
}

# ===========================================================
#  UNINSTALL
# ===========================================================
do_uninstall() {
    info "Removing auto-approve hook..."
    rm -f "$HOOK_SCRIPT"
    ok "Hook removed"

    info "Cleaning settings.json..."
    if [ -f "$SETTINGS_FILE" ] && command -v jq &>/dev/null; then
        TEMP_FILE=$(mktemp)
        jq '
          if .hooks?.PreToolUse then
            .hooks.PreToolUse |= map(select(.hooks[0].command != "~/.claude/hooks/auto-approve.sh"))
          else . end |
          if .hooks?.PreToolUse == [] then del(.hooks.PreToolUse) else . end
        ' "$SETTINGS_FILE" > "$TEMP_FILE"
        mv "$TEMP_FILE" "$SETTINGS_FILE"
        ok "Settings cleaned"
    fi
}

# ===========================================================
#  STATUS
# ===========================================================
do_status() {
    echo "Auto-Approve"
    echo ""
    if [ -f "$HOOK_SCRIPT" ] && [ -x "$HOOK_SCRIPT" ]; then
        printf '  Hook:      \033[1;32m✓ installed\033[0m\n'
    else
        printf '  Hook:      \033[1;31m✗ not installed\033[0m\n'
    fi
    if [ -f "$SETTINGS_FILE" ] && grep -q "auto-approve.sh" "$SETTINGS_FILE" 2>/dev/null; then
        printf '  Settings:  \033[1;32m✓ configured\033[0m\n'
    else
        printf '  Settings:  \033[1;31m✗ not configured\033[0m\n'
    fi
    echo ""
}

# ===========================================================
#  MAIN
# ===========================================================
cmd="${1:-}"
shift || true

case "$cmd" in
    install)   do_install ;;
    uninstall) do_uninstall ;;
    status)    do_status ;;
    *)         usage ;;
esac
