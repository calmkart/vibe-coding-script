#!/bin/bash
set -euo pipefail

# ============================================================
#  Claude Code × iTerm2 Tab Status Indicator
#
#  Usage:
#    ./iterm-status-setup.sh install           # English (default)
#    ./iterm-status-setup.sh install --lang zh  # Chinese
#    ./iterm-status-setup.sh uninstall          # Remove all changes
#    ./iterm-status-setup.sh status             # Show current state
# ============================================================

CLAUDE_DIR="$HOME/.claude"
HOOKS_DIR="$CLAUDE_DIR/hooks"
SETTINGS_FILE="$CLAUDE_DIR/settings.json"
HOOK_SCRIPT="$HOOKS_DIR/iterm-status.sh"
ITERM_PLIST="$HOME/Library/Preferences/com.googlecode.iterm2.plist"

info()  { printf '\033[1;34m[INFO]\033[0m  %s\n' "$1"; }
ok()    { printf '\033[1;32m[OK]\033[0m    %s\n' "$1"; }
warn()  { printf '\033[1;33m[WARN]\033[0m  %s\n' "$1"; }
err()   { printf '\033[1;31m[ERR]\033[0m   %s\n' "$1"; }

usage() {
    cat <<EOF
Claude Code × iTerm2 Tab Status Indicator

Usage:
  $(basename "$0") install [--lang zh]    Install (default: English)
  $(basename "$0") uninstall              Remove all changes
  $(basename "$0") status                 Show current state

Options:
  --lang zh    Use Chinese labels (执行中 / 待确认 / 等待输入)
               Default is English (Working / Action Needed / Ready)
EOF
    exit 1
}

# ===========================================================
#  INSTALL
# ===========================================================
do_install() {
    local lang="${1:-en}"

    # Resolve labels at install time
    if [ "$lang" = "zh" ]; then
        L_WORKING="执行中"; L_ATTENTION="待确认"; L_DONE="等待输入"
    else
        L_WORKING="Working"; L_ATTENTION="Action Needed"; L_DONE="Ready"
    fi

    # ------------------------------------------------------
    # 1. Pre-flight
    # ------------------------------------------------------
    info "Checking environment..."

    if ! command -v claude &>/dev/null; then
        warn "claude command not found — install Claude Code first"
    fi

    SKIP_ITERM=0
    if [ ! -f "$ITERM_PLIST" ]; then
        warn "iTerm2 plist not found — skipping iTerm2 configuration"
        SKIP_ITERM=1
    fi

    mkdir -p "$HOOKS_DIR"
    ok "Environment OK"

    # ------------------------------------------------------
    # 2. Configure iTerm2
    # ------------------------------------------------------
    if [ "$SKIP_ITERM" = "0" ]; then
        info "Configuring iTerm2 profiles..."

        PB="/usr/libexec/PlistBuddy"

        count=0
        while "$PB" -c "Print :New\ Bookmarks:${count}:Guid" "$ITERM_PLIST" &>/dev/null; do
            count=$((count + 1))
        done

        if [ "$count" -eq 0 ]; then
            warn "No iTerm2 profiles found — skipping"
        else
            for i in $(seq 0 $((count - 1))); do
                p=":New Bookmarks:${i}"

                "$PB" -c "Set '${p}:Title Components' 3" "$ITERM_PLIST" 2>/dev/null ||
                "$PB" -c "Add '${p}:Title Components' integer 3" "$ITERM_PLIST" 2>/dev/null || true

                "$PB" -c "Set '${p}:Allow Title Setting' true" "$ITERM_PLIST" 2>/dev/null ||
                "$PB" -c "Add '${p}:Allow Title Setting' bool true" "$ITERM_PLIST" 2>/dev/null || true

                # Silence bell to avoid visual flash / screen icons
                "$PB" -c "Set '${p}:Visual Bell' false" "$ITERM_PLIST" 2>/dev/null ||
                "$PB" -c "Add '${p}:Visual Bell' bool false" "$ITERM_PLIST" 2>/dev/null || true

                "$PB" -c "Set '${p}:Flashing Bell' false" "$ITERM_PLIST" 2>/dev/null ||
                "$PB" -c "Add '${p}:Flashing Bell' bool false" "$ITERM_PLIST" 2>/dev/null || true

                "$PB" -c "Set '${p}:Silence Bell' true" "$ITERM_PLIST" 2>/dev/null ||
                "$PB" -c "Add '${p}:Silence Bell' bool true" "$ITERM_PLIST" 2>/dev/null || true

                "$PB" -c "Set '${p}:BM Growl' false" "$ITERM_PLIST" 2>/dev/null ||
                "$PB" -c "Add '${p}:BM Growl' bool false" "$ITERM_PLIST" 2>/dev/null || true
            done

            ok "Configured ${count} iTerm2 profile(s)"
        fi
    fi

    # ------------------------------------------------------
    # 3. Create hook script (labels baked in)
    # ------------------------------------------------------
    info "Creating hook script (lang=$lang)..."

    cat > "$HOOK_SCRIPT" <<EOF
#!/bin/bash
# iTerm2 tab status indicator for Claude Code
# Generated by iterm-status-setup.sh (lang=$lang)

L_WORKING="$L_WORKING"; L_ATTENTION="$L_ATTENTION"; L_DONE="$L_DONE"

CACHE="/tmp/iterm-tty-\$PPID"
if [ -f "\$CACHE" ]; then
    read -r TTY < "\$CACHE"
else
    pid=\$PPID; TTY="/dev/tty"
    while [ "\$pid" -gt 1 ]; do
        t=\$(ps -o tty= -p "\$pid" 2>/dev/null | tr -d ' ')
        if [ -n "\$t" ] && [ "\$t" != "??" ] && [ -w "/dev/\$t" ]; then
            TTY="/dev/\$t"; break
        fi
        pid=\$(ps -o ppid= -p "\$pid" 2>/dev/null | tr -d ' ')
    done
    echo "\$TTY" > "\$CACHE"
fi

input=\$(cat 2>/dev/null)

# Lock the directory name to the initial cwd (never changes during a session)
DIR_CACHE="/tmp/iterm-dir-\$PPID"
if [ -f "\$DIR_CACHE" ]; then
    read -r dir < "\$DIR_CACHE"
else
    dir=""
    if [ -n "\$input" ]; then
        cwd=\$(echo "\$input" | grep -oE '"cwd" *: *"[^"]*"' | head -1 | sed 's/.*: *"//;s/"$//')
        [ -n "\$cwd" ] && dir=\$(basename "\$cwd")
    fi
    [ -n "\$dir" ] && echo "\$dir" > "\$DIR_CACHE"
fi

# For PreToolUse, only set attention for tools that actually block for user input
if [ "\$1" = "attention" ] && [ -n "\$input" ]; then
    tool=\$(echo "\$input" | grep -oE '"tool_name" *: *"[^"]*"' | head -1 | sed 's/.*: *"//;s/"$//')
    case "\$tool" in
        AskUserQuestion|EnterPlanMode|ExitPlanMode|"") ;;
        Bash)
            # git push requires confirmation per hook config
            echo "\$input" | grep -qE 'git.*push' || exit 0
            ;;
        *) exit 0 ;;
    esac
fi

# State lock: attention can only be cleared by "working" (user confirmed)
# Prevents done/notification from overriding attention
ATTENTION_LOCK="/tmp/iterm-attention-\$PPID"
STATUS="\$1"

case "\$1" in
    attention)
        echo 1 > "\$ATTENTION_LOCK"
        ;;
    working)
        rm -f "\$ATTENTION_LOCK"
        ;;
    done|"")
        if [ -f "\$ATTENTION_LOCK" ]; then
            STATUS="attention"
        fi
        ;;
    reset)
        rm -f "\$ATTENTION_LOCK" "\$DIR_CACHE"
        ;;
esac

[ -z "\$STATUS" ] && exit 0

set_title() { printf '\033]1;%s\a' "\$1" > "\$TTY" 2>/dev/null; }

case "\$STATUS" in
    working)
        printf '\033]6;1;bg;red;brightness;50\a\033]6;1;bg;green;brightness;145\a\033]6;1;bg;blue;brightness;80\a' > "\$TTY" 2>/dev/null
        set_title "◉ \${L_WORKING}\${dir:+ · \$dir}"
        ;;
    attention)
        printf '\033]6;1;bg;red;brightness;200\a\033]6;1;bg;green;brightness;150\a\033]6;1;bg;blue;brightness;50\a' > "\$TTY" 2>/dev/null
        set_title "⏸ \${L_ATTENTION}\${dir:+ · \$dir}"
        ;;
    done)
        printf '\033]6;1;bg;red;brightness;65\a\033]6;1;bg;green;brightness;105\a\033]6;1;bg;blue;brightness;200\a' > "\$TTY" 2>/dev/null
        set_title "✓ \${L_DONE}\${dir:+ · \$dir}"
        ;;
    reset)
        printf '\033]6;1;bg;*;default\a' > "\$TTY" 2>/dev/null
        set_title ""
        ;;
esac
EOF

    chmod +x "$HOOK_SCRIPT"
    ok "Hook script created: $HOOK_SCRIPT"

    # ------------------------------------------------------
    # 4. Update settings.json
    # ------------------------------------------------------
    info "Updating Claude Code settings..."

    [ -f "$SETTINGS_FILE" ] || echo '{}' > "$SETTINGS_FILE"

    python3 << 'PYEOF'
import json, os

settings_path = os.path.expanduser("~/.claude/settings.json")

with open(settings_path) as f:
    settings = json.load(f)

hooks = settings.setdefault("hooks", {})
CMD = "~/.claude/hooks/iterm-status.sh"

def hook_entry(matcher, cmd):
    return {"matcher": matcher, "hooks": [{"type": "command", "command": cmd}]}

def is_iterm_status(entry):
    return "iterm-status.sh" in json.dumps(entry)

hooks["Notification"] = [hook_entry("*", f"{CMD} done")]
hooks["UserPromptSubmit"] = [hook_entry("*", f"{CMD} working")]

posttool = [h for h in hooks.get("PostToolUse", []) if not is_iterm_status(h)]
posttool.insert(0, hook_entry("*", f"{CMD} working"))
hooks["PostToolUse"] = posttool

hooks["Stop"] = [hook_entry("*", f"{CMD} done")]

pretool = [h for h in hooks.get("PreToolUse", []) if not is_iterm_status(h)]
pretool.insert(0, hook_entry("*", f"{CMD} attention"))
hooks["PreToolUse"] = pretool

settings.setdefault("env", {})["CLAUDE_CODE_DISABLE_TERMINAL_TITLE"] = "1"

with open(settings_path, "w") as f:
    json.dump(settings, f, indent=2, ensure_ascii=False)
    f.write("\n")

PYEOF

    ok "settings.json updated"

    # ------------------------------------------------------
    # 5. Cleanup & summary
    # ------------------------------------------------------
    rm -f /tmp/iterm-status.log /tmp/iterm-tools.log 2>/dev/null

    echo ""
    echo "============================================"
    echo "  Installed! (lang=$lang)"
    echo "============================================"
    echo ""
    if [ "$lang" = "zh" ]; then
        echo "  Tab 状态："
        echo "    松绿  ◉ 执行中    — Claude 正在工作"
        echo "    琥珀  ⏸ 待确认    — 需要你操作"
        echo "    蓝色  ✓ 等待输入  — 等你发下一条消息"
    else
        echo "  Tab states:"
        echo "    Green  ◉ Working       — Claude is processing"
        echo "    Amber  ⏸ Action Needed — Waiting for your input"
        echo "    Blue   ✓ Ready         — Waiting for next message"
    fi
    echo ""
    echo "  Next: restart iTerm2, then open a new Claude Code session."
    echo "  To switch language, run: $(basename "$0") install --lang zh|en"
    echo ""
}

# ===========================================================
#  UNINSTALL
# ===========================================================
do_uninstall() {
    info "Removing hook script..."
    rm -f "$HOOK_SCRIPT"
    rm -f /tmp/iterm-tty-* /tmp/iterm-dir-* /tmp/iterm-attention-* /tmp/iterm-status.log /tmp/iterm-tools.log 2>/dev/null
    ok "Removed $HOOK_SCRIPT"

    info "Cleaning settings.json..."
    if [ -f "$SETTINGS_FILE" ]; then
        python3 << 'PYEOF'
import json, os

settings_path = os.path.expanduser("~/.claude/settings.json")

with open(settings_path) as f:
    settings = json.load(f)

hooks = settings.get("hooks", {})

def is_iterm_status(entry):
    return "iterm-status.sh" in json.dumps(entry)

# Remove dedicated hook arrays set by this tool
for key in ["Notification", "UserPromptSubmit", "Stop"]:
    entries = hooks.get(key, [])
    entries = [h for h in entries if not is_iterm_status(h)]
    if entries:
        hooks[key] = entries
    else:
        hooks.pop(key, None)

# Remove iterm-status entries from shared arrays
for key in ["PreToolUse", "PostToolUse"]:
    entries = hooks.get(key, [])
    entries = [h for h in entries if not is_iterm_status(h)]
    if entries:
        hooks[key] = entries
    else:
        hooks.pop(key, None)

# Remove env var
env = settings.get("env", {})
env.pop("CLAUDE_CODE_DISABLE_TERMINAL_TITLE", None)
if not env:
    settings.pop("env", None)

with open(settings_path, "w") as f:
    json.dump(settings, f, indent=2, ensure_ascii=False)
    f.write("\n")

PYEOF
        ok "settings.json cleaned"
    fi

    echo ""
    echo "============================================"
    echo "  Uninstalled!"
    echo "============================================"
    echo ""
    echo "  iTerm2 plist was not modified (harmless settings kept)."
    echo "  Restart iTerm2 and open a new Claude Code session."
    echo ""
}

# ===========================================================
#  STATUS
# ===========================================================
do_status() {
    echo "iTerm2 Tab Status Indicator"
    echo ""

    if [ -f "$HOOK_SCRIPT" ]; then
        lang=$(grep -o 'lang=[a-z]*' "$HOOK_SCRIPT" | head -1 | cut -d= -f2)
        printf '  Hook script:  \033[1;32minstalled\033[0m (%s)\n' "$HOOK_SCRIPT"
        printf '  Language:     %s\n' "${lang:-en}"
    else
        printf '  Hook script:  \033[1;31mnot installed\033[0m\n'
    fi

    if [ -f "$SETTINGS_FILE" ] && grep -q "iterm-status.sh" "$SETTINGS_FILE" 2>/dev/null; then
        printf '  Settings:     \033[1;32mconfigured\033[0m\n'
    else
        printf '  Settings:     \033[1;31mnot configured\033[0m\n'
    fi

    echo ""
}

# ===========================================================
#  MAIN
# ===========================================================
cmd="${1:-}"
shift || true

case "$cmd" in
    install)
        lang="en"
        while [ $# -gt 0 ]; do
            case "$1" in
                --lang) [ $# -ge 2 ] || { err "Missing value for --lang"; usage; }; lang="$2"; shift 2 ;;
                --lang=*) lang="${1#--lang=}"; shift ;;
                *) err "Unknown option: $1"; usage ;;
            esac
        done
        do_install "$lang"
        ;;
    uninstall)
        do_uninstall
        ;;
    status)
        do_status
        ;;
    *)
        usage
        ;;
esac
