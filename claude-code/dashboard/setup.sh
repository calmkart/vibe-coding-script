#!/bin/bash
set -euo pipefail

# ============================================================
#  Claude Code Terminal Dashboard
#
#  Rich TUI dashboard for managing Claude Code sessions.
#  Built with Python + Textual.
#
#  Usage:
#    ./setup.sh install      # Install dashboard
#    ./setup.sh uninstall    # Remove dashboard
#    ./setup.sh status       # Check installation status
# ============================================================

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
VENV_DIR="$HOME/.claude/dashboard-venv"
LAUNCHER="$HOME/.local/bin/claude-dashboard"

# --- Colors & helpers ---
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
BOLD='\033[1m'
NC='\033[0m'

info()  { echo -e "${BLUE}[info]${NC}  $*"; }
ok()    { echo -e "${GREEN}[ok]${NC}    $*"; }
warn()  { echo -e "${YELLOW}[warn]${NC}  $*"; }
err()   { echo -e "${RED}[error]${NC} $*"; }

# --- Pre-flight checks ---
preflight() {
    local ok=true

    if ! command -v python3 &>/dev/null; then
        err "python3 is required but not found"
        ok=false
    else
        info "python3 found: $(python3 --version)"
    fi

    if [ "$ok" = false ]; then
        err "Pre-flight checks failed. Please install missing dependencies."
        exit 1
    fi
}

# --- Install ---
do_install() {
    local lang="en"

    # Parse extra args (--lang zh)
    while [ $# -gt 0 ]; do
        case "$1" in
            --lang) lang="$2"; shift 2 ;;
            *) shift ;;
        esac
    done

    info "Installing Claude Code Dashboard (lang=$lang)..."
    echo ""

    preflight

    # Create venv
    if [ -d "$VENV_DIR" ]; then
        info "Virtual environment already exists at $VENV_DIR"
    else
        info "Creating virtual environment..."
        python3 -m venv "$VENV_DIR"
    fi

    # Install dependencies
    info "Installing Python dependencies..."
    "$VENV_DIR/bin/pip" install --quiet --upgrade pip
    "$VENV_DIR/bin/pip" install --quiet -r "$SCRIPT_DIR/requirements.txt"

    # Create launcher script with language baked in
    info "Creating launcher script..."
    mkdir -p "$(dirname "$LAUNCHER")"

    local lang_arg=""
    if [ "$lang" != "en" ]; then
        lang_arg="--lang $lang"
    fi

    cat > "$LAUNCHER" << LAUNCHER_EOF
#!/bin/bash
# Claude Code Dashboard launcher
# Generated by setup.sh (lang=$lang) — do not edit manually
exec "$VENV_DIR/bin/python" "$SCRIPT_DIR/app.py" $lang_arg "\$@"
LAUNCHER_EOF
    chmod +x "$LAUNCHER"

    echo ""
    ok "Dashboard installed successfully!"
    echo ""
    echo -e "  ${BOLD}Run:${NC}  claude-dashboard"
    echo -e "  ${BOLD}Or:${NC}   $LAUNCHER"
    echo ""

    # Check if ~/.local/bin is in PATH
    if [[ ":$PATH:" != *":$HOME/.local/bin:"* ]]; then
        warn "~/.local/bin is not in your PATH"
        echo "  Add this to your ~/.zshrc or ~/.bashrc:"
        echo "    export PATH=\"\$HOME/.local/bin:\$PATH\""
        echo ""
    fi
}

# --- Uninstall ---
do_uninstall() {
    info "Uninstalling Claude Code Dashboard..."
    echo ""

    if [ -f "$LAUNCHER" ]; then
        rm -f "$LAUNCHER"
        ok "Removed launcher: $LAUNCHER"
    else
        info "Launcher not found (already removed)"
    fi

    if [ -d "$VENV_DIR" ]; then
        rm -rf "$VENV_DIR"
        ok "Removed virtual environment: $VENV_DIR"
    else
        info "Virtual environment not found (already removed)"
    fi

    echo ""
    ok "Dashboard uninstalled."
}

# --- Status ---
do_status() {
    echo ""
    echo -e "${BOLD}Claude Code Dashboard Status${NC}"
    echo ""

    # Launcher
    if [ -f "$LAUNCHER" ]; then
        ok "Launcher: $LAUNCHER"
    else
        warn "Launcher: not installed"
    fi

    # Venv
    if [ -d "$VENV_DIR" ]; then
        ok "Virtual env: $VENV_DIR"
        # Check textual version
        if "$VENV_DIR/bin/python" -c "import textual; print(f'  Textual: {textual.__version__}')" 2>/dev/null; then
            :
        else
            warn "  Textual not installed in venv"
        fi
    else
        warn "Virtual env: not found"
    fi

    # PATH check
    if [[ ":$PATH:" == *":$HOME/.local/bin:"* ]]; then
        ok "PATH: ~/.local/bin is in PATH"
    else
        warn "PATH: ~/.local/bin not in PATH"
    fi

    echo ""
}

# --- Main ---
action="${1:-}"
shift 2>/dev/null || true

case "$action" in
    install)   do_install "$@" ;;
    uninstall) do_uninstall ;;
    status)    do_status ;;
    *)
        echo "Usage: $0 {install|uninstall|status} [--lang zh]"
        exit 1
        ;;
esac
